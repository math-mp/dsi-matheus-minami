public class operadores {
    public static void main(String[] args) {

        // exemplos de utilização de operadores
        int n1 = 120;
        int n2 = 30;

        System.out.println(n1+n1);//soma
        System.out.println(n1-n1);//subtração
        System.out.println(n1/n1);//divisão
        System.out.println(n1*n1);//multiplicação
        System.out.println(n1%n1);//modulo ou resto da divisão

        //incrementação de variavel
        ++n1;
        System.out.println(n1);

        //decrementação de variavel
        --n1;
        System.out.println(n1);

    }

}