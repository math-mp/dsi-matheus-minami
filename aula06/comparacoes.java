public class comparacoes{
    public static void main(String[] args){

        //operadores condicionais
        int x = 10;

        //operador de igualdade
        System.out.println(x == 1); //retorno: false pois x nao é igual a 1

        //operador de diferença
        System.out.println(x != 1 ); //retorno: true pois x é diferente de 1

        //operador maior que
        System.out.println(x > 1);//retorno: true pois x é maior que 1

        //operador menor que 
        System.out.println(x < 1);//retorno: false porque x é menor que 10

        //operador maior ou igual a
        System.out.println(x >= 1); //retorno: true pois 10 é maior que 1 
        
        //operador menor ou igual a 
        System.out.println(x <= 1); //retorno: false pois 10 não é menor que 1 nem igual a 1

    }
}
